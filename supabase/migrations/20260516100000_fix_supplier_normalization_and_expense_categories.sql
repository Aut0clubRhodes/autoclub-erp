create table if not exists public.expense_categories (
  id bigint generated by default as identity primary key,
  name text not null unique,
  created_at timestamptz not null default now()
);

alter table public.expense_categories enable row level security;

drop policy if exists "Allow read expense categories" on public.expense_categories;
create policy "Allow read expense categories"
on public.expense_categories
for select
using (true);

drop policy if exists "Allow insert expense categories" on public.expense_categories;
create policy "Allow insert expense categories"
on public.expense_categories
for insert
with check (true);

