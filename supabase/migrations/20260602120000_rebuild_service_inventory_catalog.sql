insert into public.service_inventory_service_type_materials (service_type_id, material_type_id)
select service_type.id, material_type.id
from public.service_inventory_service_types service_type
join public.service_inventory_material_types material_type
  on (
    (service_type.name = 'Service / Λάδια' and material_type.name in ('Λάδι', 'Φίλτρο λαδιού', 'Φίλτρο αέρα', 'Φίλτρο καμπίνας'))
    or (service_type.name = 'Φρένα' and material_type.name in ('Τακάκια', 'Δισκόπλακες', 'Υγρά φρένων'))
    or (service_type.name = 'Ελαστικά' and material_type.name = 'Ελαστικά')
    or (service_type.name = 'Μπαταρία' and material_type.name = 'Μπαταρία')
    or (service_type.name = 'Άλλο' and material_type.name = 'Άλλο')
  )
on conflict (service_type_id, material_type_id) do nothing;
